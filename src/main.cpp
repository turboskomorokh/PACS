bool checkKey(unsigned char *key)
{
	for (int i = 0; i < 4; i++)
	{
		if (KnownKey[0][i] != key[i])
		{
			return false;
		}
	}
	return true;
}

void checkRfid()
{
#if RFID_ENABLE == 1
	if (!rfid.isCardPresent())
		return;

	unsigned char *uidBytes = rfid.ReadCard();

	if (checkKey(uidBytes))
	{
#if BUZZER_ENABLE == 1
		buzzer.beep(BEEP_SUCCESS);
#endif
		display.clearDisplay();
		display.drawBitmap(0, 0, SuccessBMP, 128, 64, SSD1306_WHITE);
	}
	else
	{
#if BUZZER_ENABLE == 1
		buzzer.beep(BEEP_FAILURE);
#endif
		display.clearDisplay();
		display.drawBitmap(0, 0, FailureBMP, 128, 64, SSD1306_WHITE);
	}

	display.display();

	Serial.print("UID: ");
	for (int i = 0; i < rfid.mfrc522.uid.size; i++)
	{
		Serial.print("0x");
		if (uidBytes[i] < 0x10)
		{
			Serial.print("0");
		}
		Serial.print(uidBytes[i], HEX);
		Serial.print(" ");
	}
	Serial.println("");
#endif
}

void setup()
{
	Serial.begin(115200);
	display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
	display.clearDisplay();
	display.display();
	Serial.println("Сканируйте карту...");
	display.setTextSize(1);
	display.setTextColor(SSD1306_WHITE);
	display.setCursor(0, 0);

	RTOS_Task rfidTask(checkRfid, 10, 1000, (Byte)0);
	rtos.add(rfidTask);
}

void loop()
{
	rtos.run();
}
